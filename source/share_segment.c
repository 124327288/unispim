int LoadSharedSegment()
{
	//先看内存里有没有
	if (share_segment)
		return 1;
	else
		share_segment = GetSharedMemory(segment_share_name);

	//如果没有，应该是还没有加载到共享内存，加载之
	if (!share_segment)
	{
		share_segment = AllocateSharedMemory(segment_share_name, sizeof(SHARE_SEGMENT));

		//如果还是没有，出错了
		if (!share_segment)
			return 0;

		//把默认数据项复制过去
		memcpy(share_segment, &default_share_segment, sizeof(SHARE_SEGMENT));

		share_segment->syllable_map_items   = sizeof(share_segment->syllable_map) / sizeof(share_segment->syllable_map[0]);
		share_segment->syllablev5_map_items = sizeof(share_segment->syllablev5_map) / sizeof(share_segment->syllablev5_map[0]);
	}

	return 1;
}

int FreeSharedSegment()
{
	if (share_segment && (GetWindowVersion() > 0x0500))
	{
		FreeSharedMemory(segment_share_name, share_segment);
		share_segment = 0;
	}

	return 1;
}